    public GameObject treeObj;

    private void Start()
    {
        float x = 0.9f;
        float y = 0.9f;

        int number = 0;

        //맵 설정
        for (int j = 0; j < 87; j++)
        {
            for (int i = 0; i < 50; i++)
            {
                //이동 가능한 땅 타일
                if (mapPos[j, i] == 1)
                {
                    GameObject tempObj = Instantiate(csInitData.instance.grass2, transform);
                    tempObj.transform.position = new Vector3(-15 + x * i, -30 + (y * 87) - y * j, 0);

                    if (Random.Range(0, 5) == 1)
                    {
                        if (j == 50 && i == 19)
                        {
                            csTile setTile = tempObj.GetComponent<csTile>();
                            setTile.tileMove = true;
                            setTile.SetPos(j, i, number);
                            csPlayerCtrl.instance.tiles[j, i] = setTile;
                        }
                        else
                        {
                            GameObject tempTree = Instantiate(csInitData.instance.tree[Random.Range(6, 9)], treeObj.transform);

                            csTile setTile = tempObj.GetComponent<csTile>();
                            setTile.tileMove = true;
                            setTile.SetPos(j, i, number);
                            csPlayerCtrl.instance.tiles[j, i] = setTile;

                            csTree cstempTree = tempTree.GetComponent<csTree>();
                            cstempTree.pos = setTile;
                            setTile.tree = cstempTree;
                            setTile.havetree = true;

                            tempTree.transform.position = new Vector2(tempObj.transform.position.x + cstempTree.x, tempObj.transform.position.y + cstempTree.y);
                            tempTree.GetComponent<SpriteRenderer>().sortingOrder = j;
                        }
                    }
                    else
                    {
                        csTile setTile = tempObj.GetComponent<csTile>();
                        setTile.tileMove = true;
                        setTile.SetPos(j, i, number);
                        csPlayerCtrl.instance.tiles[j, i] = setTile;
                    }
                }
                //이동 불가능한 물 타일
                else if (mapPos[j, i] == 0)
                {

                    GameObject tempObj = Instantiate(csInitData.instance.water, this.transform);
                    tempObj.transform.position = new Vector3(-15 + x * i, -30 + (y * 87) - y * j, 0);

                    csTile setTile = tempObj.GetComponent<csTile>();
                    setTile.tileMove = false;
                    setTile.SetPos(j, i, number);
                    csPlayerCtrl.instance.tiles[j, i] = setTile;
                }
                number++;
            }
        }
    }
